package com.example.amazon.Model;

public class Cart {


    private String pid , ProductName , Price , quantity , data , time , discount ;

    public Cart() {
    }

    public Cart(String pid, String productName, String price, String quantity, String data, String time, String discount) {
        this.pid = pid;
        ProductName = productName;
        Price = price;
        this.quantity = quantity;
        this.data = data;
        this.time = time;
        this.discount = discount;
    }

    public String getPid() {
        return pid;
    }

    public void setPid(String pid) {
        this.pid = pid;
    }

    public String getProductName() {
        return ProductName;
    }

    public void setProductName(String productName) {
        ProductName = productName;
    }

    public String getPrice() {
        return Price;
    }

    public void setPrice(String price) {
        Price = price;
    }

    public String getQuantity() {
        return quantity;
    }

    public void setQuantity(String quantity) {
        this.quantity = quantity;
    }

    public String getData() {
        return data;
    }

    public void setData(String data) {
        this.data = data;
    }

    public String getTime() {
        return time;
    }

    public void setTime(String time) {
        this.time = time;
    }

    public String getDiscount() {
        return discount;
    }

    public void setDiscount(String discount) {
        this.discount = discount;
    }
}
